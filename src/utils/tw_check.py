"""繁體中文檢查工具。

偵測生成文本中的常見簡體字，供 CLI 警告使用。
"""

# 常見簡體→繁體對照（僅覆蓋高頻且在公文中常出現的字）
_SC_TO_TC: dict[str, str] = {
    "应": "應", "协": "協", "将": "將", "关": "關", "认": "認",
    "办": "辦", "动": "動", "员": "員", "问": "問", "发": "發",
    "对": "對", "组": "組", "织": "織", "机": "機", "构": "構",
    "规": "規", "划": "劃", "实": "實", "施": "施", "备": "備",
    "报": "報", "审": "審", "查": "查", "处": "處", "负": "負",
    "责": "責", "执": "執", "行": "行", "标": "標", "准": "準",
    "须": "須", "环": "環", "境": "境", "资": "資", "产": "產",
    "经": "經", "济": "濟", "财": "財", "务": "務", "营": "營",
    "业": "業", "证": "證", "许": "許", "设": "設", "计": "計",
    "达": "達", "进": "進", "请": "請", "与": "與", "专": "專",
    "门": "門", "长": "長", "会": "會", "议": "議", "决": "決",
    "订": "訂", "条": "條", "项": "項", "总": "總", "结": "結",
    "论": "論", "义": "義", "权": "權", "制": "制", "国": "國",
}


def detect_simplified(text: str) -> list[tuple[str, str, int]]:
    """偵測文本中的簡體字。

    Returns:
        list of (簡體字, 建議繁體字, 在文本中的位置)
    """
    findings: list[tuple[str, str, int]] = []
    for i, ch in enumerate(text):
        if ch in _SC_TO_TC:
            findings.append((ch, _SC_TO_TC[ch], i))
    return findings
